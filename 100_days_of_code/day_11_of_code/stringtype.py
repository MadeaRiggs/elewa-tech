var= "My name is Jane"

#length of variable
print("length of variable is: ", len(var))

#lower case
print("lower case of variable is: ", var.lower())

#upper case
print("upper case of variable is: ", var.upper())

#splitting
print("splitted variable is: ", var.split())

#strip
print("stripped variable is: ", var.strip(""))

#replacing
print("replaced variable is: ", var.replace("Jane", "Alice"))

#concatenation
str_1= " She is good"
print("concatenated variable is: ", var + str_1)

#datatype identification
a= 54
